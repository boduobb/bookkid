function getRandomResponse(category, subCategory = null) {
  let responses;
  if (subCategory) {
    responses = aiResponses[category]?.[subCategory];
  } else {
    responses = aiResponses[category];
  }
  if (!responses || responses.length === 0) {
    return '好的～';
  }
  return responses[Math.floor(Math.random() * responses.length)];
}

