export const getAIResponse = (bookType: BookType, category: keyof AIResponseTemplate): string => {
  const responses = baseResponses[bookType][category];
  return responses[Math.floor(Math.random() * responses.length)];
};

export const generateRetellingFollowUp = (bookType: BookType, userInput: string): string => {
  const lowerInput = userInput.toLowerCase();
  
  if (lowerInput.includes('喜欢') || lowerInput.includes('最爱')) {
    return '哇，你真的很喜欢呢！能说说为什么喜欢吗？';
  }
  if (lowerInput.includes('因为') || lowerInput.includes('所以')) {
    return '你说得好有道理！还有吗？我还想听～';
  }
  if (userInput.length < 20) {
    return '嗯～ 还有吗？再多说一点点给我听好不好？🥺';
  }
  
  return getAIResponse(bookType, 'encouragement');
};
